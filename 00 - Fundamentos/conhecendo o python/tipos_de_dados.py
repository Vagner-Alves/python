str()                               # Texto
int(), float(), complex()           # Números
list(), tuple(), range()            # Sequencias 
dict()                              # Mapa ( mapear valores)
set(), frozenset()                  # Coleção 
bool()                              # Booleano ( verdadeiro ou falso)
bytes(), bytearray(), memoryview()  # Binário

