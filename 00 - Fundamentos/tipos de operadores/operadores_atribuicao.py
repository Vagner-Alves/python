'''
OPERADORES DE ATRIBUIÇÃO
são operadores utilizados para definir o valor inicial ou sobrescrever o valor de uma variável.
Temos as atribuições simples ( = ) ou com operadores matemáticos 
exemplos:
+= , -= , //= , /= , *= , %= , **=

'''

saldo = 1000 # atribuição simples

# soma
saldo += 200
print(saldo)

# subtração
saldo -= 200
print(saldo)

# divisão
saldo //= 2
print(saldo)

# divisão inteira
saldo /= 2
print(saldo)

# multiplicação
saldo *= 3
print(saldo)

# Exponenciação
saldo **= 2
print(saldo)

# módulo
saldo %= 2
print(saldo)
