'''
Um Set é uma coleção que não possui objetos repetidos, usamos set para representar conjuntos matemáticos
ou eliminar itens duplicados de um iteravél. O Set não garante a ordem dos elementos.
'''

conjunto_numerico = set([1,2,3,3,4,6,5,2])
linguagens_programacao = set(["java","python","js","java","python"])

print(conjunto_numerico)
print(linguagens_programacao)
