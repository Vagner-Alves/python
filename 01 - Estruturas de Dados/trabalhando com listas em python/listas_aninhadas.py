'''
LISTAS ANINHADAS
listas podem armazenar todos os tipos de objeto python, portanto podemos ter listas que armazenam outras listas.
Com isso podemos criar estruturas bidimensionais ( tabelas ), e acessar informando os indices de linha e coluna.
'''

matriz = [
    [1,  'a', 2 ] ,
    ['b', 3 , 4],
    [5,   6, 'c']
]

print(matriz[0])
print(matriz[0][0])
print(matriz[0][1])
print(matriz[0][2])